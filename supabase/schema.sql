-- FemboyChat — production Supabase schema (applied to project azriyxvofeceosuoptcm).
--
-- This mirrors what the SupabaseBackend (src/lib/backend/supabase.ts) expects.
-- Secret VALUES (model API keys, webhook secret) live in Supabase Vault and are
-- NOT stored here; see the bottom of this file for how they are referenced.

create extension if not exists pgcrypto;
create extension if not exists pg_net;

-- ── Tables ────────────────────────────────────────────────────────────────
create table if not exists public.profiles (
  uid uuid primary key,                          -- = auth.users.id for real users; fixed ids for seeds/bots
  num_id bigint generated by default as identity,
  username text unique not null,
  name text not null,
  email text unique,
  bio text default '',
  emoji text default '🎀',
  color text default '#ff7ab8',
  status text default '',
  verified boolean default false,
  is_bot boolean default false,
  settings jsonb default '{}'::jsonb,
  last_seen timestamptz default now(),
  created_at timestamptz default now()
);

create table if not exists public.chats (
  id text primary key,
  type text not null,                            -- dm | bot | group | channel | saved
  title text not null,
  username text unique,
  emoji text default '💬',
  color text default '#ff7ab8',
  description text,
  member_uids uuid[] default '{}',
  admin_uids uuid[] default '{}',
  owner_uid uuid,
  verified boolean default false,
  member_count int default 1,
  created_at timestamptz default now()
);

create table if not exists public.messages (
  id uuid primary key default gen_random_uuid(),
  chat_id text references public.chats(id) on delete cascade,
  sender_uid uuid references public.profiles(uid) on delete set null,
  text text default '',
  reply_to_id uuid,
  reactions jsonb default '[]'::jsonb,           -- [{emoji, uids[]}]
  poll jsonb,                                    -- {question, options:[{text, uids[]}], multi}
  sticker text,
  pinned boolean default false,
  deleted boolean default false,
  read_by_uids uuid[] default '{}',
  ttl int,
  edited_ts bigint,
  ts timestamptz default now()
);

create index if not exists messages_chat_ts_idx on public.messages (chat_id, ts asc);
create index if not exists chats_member_uids_idx on public.chats using gin (member_uids);
create index if not exists profiles_username_idx on public.profiles (username);

-- ── Public directory (people, bots, groups, channels) ──────────────────────
create or replace view public.directory as
  select p.uid::text as uid,
         (case when p.is_bot then 'bot' else 'user' end) as kind,
         p.num_id::bigint as num_id, p.username, p.name, p.emoji, p.color,
         coalesce(p.bio, '') as bio, p.verified,
         null::int as members,
         (p.last_seen > now() - interval '5 minutes') as online
  from public.profiles p
  union all
  select c.id as uid, c.type as kind, 0::bigint as num_id,
         coalesce(c.username, '') as username, c.title as name, c.emoji, c.color,
         coalesce(c.description, '') as bio, c.verified,
         c.member_count as members, null::boolean as online
  from public.chats c
  where c.type in ('group', 'channel');

