-- ── RPCs (called by SupabaseBackend) ────────────────────────────────────
create or replace function public.list_my_chats()
returns setof public.chats language sql security definer stable set search_path = public as $$
  select c.* from public.chats c
  where auth.uid() = any(c.member_uids) or (c.type = 'saved' and c.owner_uid = auth.uid())
  order by coalesce((select max(m.ts) from public.messages m where m.chat_id = c.id), c.created_at) desc
$$;

create or replace function public.open_dm(other uuid)
returns public.chats language plpgsql security definer set search_path = public as $$
declare me uuid := auth.uid(); did text; ch public.chats; o public.profiles;
begin
  if me is null then raise exception 'Authentication required'; end if;
  if other = me then raise exception 'Cannot DM yourself'; end if;
  did := 'dm-' || (select string_agg(x, '~') from (select unnest(array[me::text, other::text]) as x order by x) s);
  select * into ch from public.chats where id = did;
  if found then return ch; end if;
  select * into o from public.profiles where uid = other;
  insert into public.chats (id, type, title, emoji, color, member_uids, admin_uids, created_at)
  values (did, case when coalesce(o.is_bot, false) then 'bot' else 'dm' end,
          coalesce(o.name, 'Чат'), coalesce(o.emoji, '💬'), coalesce(o.color, '#ff7ab8'),
          array[me, other], '{}', now())
  returning * into ch;
  return ch;
end $$;

create or replace function public.join_entity(entity text)
returns public.chats language plpgsql security definer set search_path = public as $$
declare me uuid := auth.uid(); p public.profiles; ch public.chats;
begin
  if me is null then raise exception 'Authentication required'; end if;
  select * into p from public.profiles where uid::text = entity;
  if found then return public.open_dm(p.uid); end if;
  select * into ch from public.chats where id = entity;
  if not found then raise exception 'Entity not found'; end if;
  if not (me = any(ch.member_uids)) then
    update public.chats set member_uids = array_append(member_uids, me), member_count = coalesce(member_count, 0) + 1
      where id = entity returning * into ch;
  end if;
  return ch;
end $$;

create or replace function public.leave_chat(chat text)
returns void language plpgsql security definer set search_path = public as $$
declare me uuid := auth.uid();
begin
  update public.chats set member_uids = array_remove(member_uids, me),
    member_count = greatest(0, coalesce(member_count, 1) - 1) where id = chat;
end $$;

create or replace function public.toggle_reaction(message uuid, emoji text)
returns void language plpgsql security definer set search_path = public as $$
declare me text := auth.uid()::text; arr jsonb; el jsonb; uids jsonb; newarr jsonb := '[]'::jsonb; found boolean := false;
begin
  select coalesce(reactions, '[]'::jsonb) into arr from public.messages where id = message;
  for el in select * from jsonb_array_elements(arr) loop
    if el->>'emoji' = emoji then
      found := true; uids := el->'uids';
      if uids @> to_jsonb(array[me]) then
        uids := (select coalesce(jsonb_agg(u), '[]'::jsonb) from jsonb_array_elements_text(uids) u where u <> me);
      else uids := uids || to_jsonb(array[me]); end if;
      if jsonb_array_length(uids) > 0 then newarr := newarr || jsonb_build_object('emoji', emoji, 'uids', uids); end if;
    else newarr := newarr || el; end if;
  end loop;
  if not found then newarr := newarr || jsonb_build_object('emoji', emoji, 'uids', to_jsonb(array[me])); end if;
  update public.messages set reactions = newarr where id = message;
end $$;

create or replace function public.vote_poll(message uuid, option_index int)
returns void language plpgsql security definer set search_path = public as $$
declare me text := auth.uid()::text; p jsonb; multi boolean; opt jsonb; uids jsonb; newopts jsonb := '[]'::jsonb; i int := 0;
begin
  select poll into p from public.messages where id = message;
  if p is null then return; end if;
  multi := coalesce((p->>'multi')::boolean, false);
  for opt in select * from jsonb_array_elements(p->'options') loop
    uids := opt->'uids';
    if i = option_index then
      if uids @> to_jsonb(array[me]) then
        uids := (select coalesce(jsonb_agg(u), '[]'::jsonb) from jsonb_array_elements_text(uids) u where u <> me);
      else uids := uids || to_jsonb(array[me]); end if;
    elsif not multi then
      uids := (select coalesce(jsonb_agg(u), '[]'::jsonb) from jsonb_array_elements_text(uids) u where u <> me);
    end if;
    newopts := newopts || jsonb_build_object('text', opt->>'text', 'uids', uids);
    i := i + 1;
  end loop;
  update public.messages set poll = jsonb_set(p, '{options}', newopts) where id = message;
end $$;

create or replace function public.toggle_pin(message uuid)
returns void language plpgsql security definer set search_path = public as $$
begin update public.messages set pinned = not coalesce(pinned, false) where id = message; end $$;

create or replace function public.mark_read(chat text)
returns void language plpgsql security definer set search_path = public as $$
declare me uuid := auth.uid();
begin
  update public.messages set read_by_uids = array_append(read_by_uids, me)
    where chat_id = chat and not (me = any(read_by_uids));
end $$;

