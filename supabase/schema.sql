-- ── FemboyAI server-side auto-reply ───────────────────────────────────────
-- Reads the model key + webhook secret from Vault (values set out-of-band):
--   vault: GEMINI_API_KEY | GROQ_API_KEY | OPENROUTER_API_KEY | GITHUB_MODELS_TOKEN
--   vault: FC_WEBHOOK_SECRET (random; shared between this trigger and the function)
create or replace function public.get_ai_config()
returns jsonb language sql security definer set search_path = '' as $$
  select coalesce(jsonb_object_agg(name, decrypted_secret), '{}'::jsonb)
  from vault.decrypted_secrets
  where name in ('GEMINI_API_KEY', 'GROQ_API_KEY', 'OPENROUTER_API_KEY', 'GITHUB_MODELS_TOKEN')
$$;
create or replace function public.get_fc_webhook_secret()
returns text language sql security definer set search_path = '' as $$
  select decrypted_secret from vault.decrypted_secrets where name = 'FC_WEBHOOK_SECRET' limit 1
$$;
revoke all on function public.get_ai_config() from public, anon, authenticated;
revoke all on function public.get_fc_webhook_secret() from public, anon, authenticated;
grant execute on function public.get_ai_config() to service_role;
grant execute on function public.get_fc_webhook_secret() to service_role;

-- On a new message in a FemboyAI bot chat, call the femboy-ai-fc Edge Function.
create or replace function public.fc_trigger_femboy()
returns trigger language plpgsql security definer set search_path = public as $$
declare fem uuid := '00000000-0000-4000-8000-000000000002'; ch public.chats; secret text;
begin
  if NEW.sender_uid = fem then return NEW; end if;
  select * into ch from public.chats where id = NEW.chat_id;
  if not found or ch.type <> 'bot' or not (fem = any(ch.member_uids)) then return NEW; end if;
  select decrypted_secret into secret from vault.decrypted_secrets where name = 'FC_WEBHOOK_SECRET' limit 1;
  perform net.http_post(
    url := 'https://azriyxvofeceosuoptcm.supabase.co/functions/v1/femboy-ai-fc',
    body := jsonb_build_object('chatId', NEW.chat_id),
    headers := jsonb_build_object('Content-Type', 'application/json', 'x-fc-secret', coalesce(secret, '')),
    timeout_milliseconds := 30000
  );
  return NEW;
end $$;

drop trigger if exists fc_femboy_reply on public.messages;
create trigger fc_femboy_reply after insert on public.messages
for each row execute function public.fc_trigger_femboy();

