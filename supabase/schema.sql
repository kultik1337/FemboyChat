-- ── Onboarding: Saved space + FemboyAI DM + auto-join communities ─────────
create or replace function public.fc_onboard_profile()
returns trigger language plpgsql security definer set search_path = public as $$
declare fem uuid := '00000000-0000-4000-8000-000000000002'; did text;
begin
  if NEW.is_bot then return NEW; end if;
  insert into public.chats (id, type, title, emoji, color, member_uids, admin_uids, owner_uid)
  values ('saved-' || NEW.uid::text, 'saved', 'Избранное', '🔖', '#7cc4ff', array[NEW.uid], array[NEW.uid], NEW.uid)
  on conflict (id) do nothing;
  did := 'dm-' || (select string_agg(x, '~') from (select unnest(array[NEW.uid::text, fem::text]) as x order by x) s);
  insert into public.chats (id, type, title, emoji, color, member_uids, admin_uids)
  values (did, 'bot', 'FemboyAI', '🎀', '#ff7ab8', array[NEW.uid, fem], '{}') on conflict (id) do nothing;
  insert into public.messages (chat_id, sender_uid, text)
  select did, fem, 'Приветик, ' || NEW.name || '! 🎀 Я FemboyAI — твой ласковый ути-пути фембойчик. Твой номер аккаунта #' || NEW.num_id || '. Напиши мне что угодно, уии~ 💗'
  where not exists (select 1 from public.messages where chat_id = did);
  update public.chats set member_uids = array_append(member_uids, NEW.uid), member_count = coalesce(member_count, 0) + 1
    where id in ('chan-news', 'grp-lounge') and not (NEW.uid = any(member_uids));
  return NEW;
end $$;

