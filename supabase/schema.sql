create policy profiles_update on public.profiles for update to authenticated using (uid = auth.uid()) with check (uid = auth.uid());

create policy chats_select on public.chats for select using (true);
create policy chats_insert on public.chats for insert to authenticated with check (owner_uid = auth.uid());
create policy chats_update on public.chats for update to authenticated
  using (owner_uid = auth.uid() or auth.uid() = any(admin_uids))
  with check (owner_uid = auth.uid() or auth.uid() = any(admin_uids));

create policy messages_select on public.messages for select using (
  exists (select 1 from public.chats c where c.id = messages.chat_id and auth.uid() = any(c.member_uids))
);
create policy messages_insert on public.messages for insert to authenticated with check (sender_uid = auth.uid());
create policy messages_update on public.messages for update to authenticated
  using (sender_uid = auth.uid()) with check (sender_uid = auth.uid());

